package cz.matej.lukac.tul.stin.cv01.model;

public class Data {
    public Data(int i, float f, String s) {
        this.i = i;
        this.f = f;
        this.s = s;
    }
    private int i;
    private float f;
    private String s;

    public int getI() {
        return i;
    }

    public float getF() {
        return f;
    }

    public String getS() {
        return s;
    }


}
